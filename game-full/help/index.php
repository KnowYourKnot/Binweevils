                <h4 id="account" class="title is-3">How do I create an account?</h4>
                  <p>In order to create an account on Bin Weevils Rewritten, you'll need to visit our new player/create account page on a PC or Laptop, as Bin Weevils Rewritten is not supported on mobile or tablet.</p>
                  <p>When you're creating an account, you'll need to come up with a Bin Weevil name (username), so a name that you don't mind other Bin Weevils seeing. Therefore, make sure you pick a name that does not contain any personal information, such as your real name.</p>
                  <p><b>Username limitations:</b></p>
                  <li>Usernames cannot contain any profanity.</li>
                  <li>Usernames have to start with at least 2 alphabetic characters.</li>
                  <li>Usernames have to be at least 3 characters long.</li>
                  <li>Multiple repeated characters are not allowed, for example: 'oooooooooo'.</li>
                  <li>Usernames can only contain up to 16 characters.</li>
                  <li>The number of '-' and '_' characters in usernames have been limited to 2.</li>
                  <li>Usernames cannot contain any special characters other than dashes and underscores.</li>
                  <li>Usernames cannot contain more than 4 numbers.</li>
                  <p><b>Users caught attempting to bypass these limitations will be banned.</b></p>
                  <p>Also, you'll need to set your account password. Remember that passwords should NEVER be shared with anyone, with exception to your parents or legal guardian.</p>
                  <p>Finally, once you've created your account, you'll enter the Bin where you can begin your adventure!</p>
                </div>


                  <h4 id="parents" class="title is-3">Parents & Safety</h4>
                    <p>On Bin Weevils Rewritten we do not tolerate any form of bullying or the use of inappropriate language.</p>
                    <p>In addition to live moderators, we employ strict automated chat filters to prevent users from typing inappropriate words and phrases. The Bin Weevils Rewritten chat moderation process is based on a ticket system. A user may accumulate a number of tickets over a period of time. These tickets are scored depending on the severity of the word used. When a user reaches a particular score they are warned and if this is ignored a number of bans are activated, 10 minutes, 60 minutes, 24 hours and a final permanent ban.</p>
                    <p>If you do discover any bad behaviour in-game we would ask that you use the report button or email the Bin Weevils user names to us so a moderator may investigate the appropriate in-game chat logs. You may also wish to add the user to your ignore list, this will prevent the user from chatting to you in-game.</p>
                    <p><b>To report another user or add them to your ignore list:</b></p>
                    <li>Click on the Weevil character responsible for the bad behaviour.</li>
                    <li>Select the unhappy blue face to place that Weevil on ignore.</li>
                    <li>Select the blue siren to send us a report after providing some further information.</li>

                    <p><b>Please take a moment to review the Bin Weevils Rewritten chat rules below:</b></p>
                    <li>Do not use bad language or post any content that may be considered offensive or bullying to anyone else.</li>
                    <li>Do not post content about topics such as dating, boyfriends/girlfriends, relationships, etc. - Bin Weevils Rewritten is a children's website and this type of content is not allowed.</li>
                    <li>Do not post any personal details or contact info, including your real name, age, school, location, address, phone number, email address, or usernames/handles on other online services or games.</li>
                    <li>Do not post any information about cheating within the Bin Weevils Rewritten game - this is against the rules.</li>
                    <li>Do not post links to other websites or online services.</li>
                    <li>If our moderators find evidence of rule breaking then action will be taken against the user.</li>

                  </div>

                  <div class="box">
                    <h4 id="currency" class="title is-3">Mulch & Dosh</h4>
                      <p>In the Binscape there are 2 types of in-game money. Firstly there is Mulch, which is available to everyone and can be earned continuously through playing Bin Weevils Rewritten.</p>
                      <p>Secondly there is Dosh, which is currency you can use to buy special things in the game such as hats and premium items for your nest.</p>
                      <p>Both of these currencies can be purchased from our <a href="topups.html"><b>Topups</b></a> page.</p>
                    </div>

                    <div class="box">
                      <h4 id="levels" class="title is-3">Levels & XP</h4>
                        <p>To level up, you’ll need to earn enough XP points. Hover the mouse over your level icon in the top-left corner of the game screen to see how much XP you need to reach the next level. Maybe you’re closer than you think!</p>
                        <p><b>Here are some of the best ways to earn XP:</b></p>
                        <li>Buying nest items in the Shopping Mall.</li>
                        <li>Getting your nest rooms rated by other Bin Weevils (the more rooms you have, the more XP you will earn).</li>
                        <li>Taking part in the Daily Brain Strain in Lab's Lab.</li>
                        <li>Playing multiplayer games.</li>
                        <li>Taking part in SWS (Secret Weevil Service) missions.</li>
                        <li>Planting and harvesting your garden.</li>
                      </div>

                      <div class="box">
                        <h4 id="codes" class="title is-3">Secret Codes</h4>
                          <p>There are two ways to enter secret codes!</p>
                          <p>The first (and easiest) way is to scroll down once you've logged in and click on the Secret Code button directly beneath the chat box.</p>
                          <p>If you want to take a more scenic route, click on Lab’s Lab from the Binscape Map and visit the Mystery Code Machine. You’ll then be able to enter your Secret Code and unlock your reward!</p>
                        </div>

                        <div class="box">
                          <h4 id="hats" class="title is-3">Hats</h4>
                            <p>In order to buy a hat, you'll need to visit Hem's Hat shop, which is located inside of Dosh's Palace.</p>
                            <p>All weevils will recieve a FREE black top hat upon registering their account.</p>
                          </div>

                  <div class="box">
                    <h4 id="nests" class="title is-3">Nests</h4>
                      <p>Your Nest items are stored in your My Stuff Box, the golden chest located on the bottom-left corner of the screen when you enter your Nest rooms. Simply click on that chest to see the latest items you’ve bought or received. You can also browse all your items by clicking on each My Stuff Box category.</p>
                      <p>To use an item, click and drag the item out of your inventory and position it in your Nest room. Release the mouse button to place the item in that location.</p>
                      <p>To put an item away, click your My Stuff Box to open it up, then click the item you want to put away and drag it down into the My Stuff Box. To help you find the item again, the My Stuff Box will show you the category that item is stored in. Very handy!</p>
                      <p>Try combining different types to create truly unique rooms! You can even put some smaller items on top of cabinets, tables and shelves!</p>
                      <p>In order to purchase Nest items, you'll need to visit either Nestco or Bin Mart, which is inside the Shopping Mall.</p>
                      <p>Some items have a level requirement – this means you’ll need to reach or be above a particular level to purchase those items. You can check your current level in the top-right corner of the game screen.</p>
                      <p>You can buy more Nest rooms by visiting Rigg’s Property Shop which is located inside the Shopping Mall. More rooms will help boost your Nest Coolness and give visitors more places to explore when you invite them over.</p>
                      <p>The maximum number of rooms you can purchase is 8.</p>
                    </div>
                    <div class="box">
                      <h4 id="gardens" class="title is-3">Gardens</h4>
                        <p>In order to access your garden, you'll need to use the Garden Pipe located within your Nest hallway. You can also access your garden from Flum's Fountain. Just click to go down the bottom left pathway.</p>
                        <p>If you'd like to buy seed and items for your garden, you'll need to take a trip to Tab's Garden Shop, which is located inside of the Shopping Mall. Here you will find lots of different seeds and items to decorate your garden.</p>
                        <p>In order to plant a seed or place a garden item, you'll firstly need to open your My Stuff Box (the yellow chest - also known as inventory). Once opened, simply click and drag the plant or item to the location you wish.</p>
                        <p>Be aware that once planted, only some items can then be moved such as statues. Other items/plants will either need to fully grow so you may harvest OR you may click and drag an item/plant back to your inventory bar, which will allow you the option to throw the item away.</p>
                        <p>To harvest your plants, they firstly must be fully grown. Remember that some plants will take longer than others to grow and by clicking a plant, you'll see the growth progression.</p>
                        <p>Once a plant has fully grown, again you'll need to click the plant and then click harvest. Beware that when you harvest some plants, they will not regrow unless you plant more.</p>
                        <p>There are however, some items that will grow again once harvested. You can find out which plants or items will regrow when you go to the 'Garden Shop' to purchase seeds or items.</p>
                        <p>In order to extend your garden, you'll need to take a trip to the Garden Inspector's 'Garden Plots' store, which is located inside the Shopping Mall.</p>
                        <p>Each Garden Plot size is priced in Mulch and there are 4 sizes available for purchase.</p>
                      </div>
                    
                    <div class="box">
                      <h4 id="plazas" class="title is-3">Plazas</h4>
                      <p>Tycoon Plazas are large areas that each weevil gets to decorate and showcase to other users.</p>
                      <p>A Tycoon Plaza can also earn you bonus Mulch. That's right - opening your Plaza to the public will earn you Mulch for every guest. The Mulch you've earned can be collected from any of the in-game cash machines, so create the best Tycoon Plaza you can!</p>
                      <p>You can view a list of open Tycoon Plazas by heading to the Shopping Mall and clicking on the top hat outside.</p>
                      <p>To open your own Plaza so other Bin Weevils Rewritten can visit and help earn you extra Mulch, all you have to do is click the red padlock icon in the main area to turn it green!</p>
                      </div>

                      <div class="box">
                        <h4 id="contests" class="title is-3">Contests</h4>
                          <p>Best Nest is a contest hosted by the Nest Inspector, where some featured Weevils who enter the contest, will have one of their Nest rooms featured within the Nest Inspector's magazine.</p>
                          <p>The Weevils featured within the Best Nest magazine will also be issued a special in-game Nest item.</p>
                          <p>Weevil of the Week is a weekly contest hosted by Scribbles on Weevil News (The Bin Weevils Blog).</p>
                          <p>Every week a Weevil that has entered the contest is selected to become Weevil of the Week. This winner is then issued a special in-game trophy, their Weevil is super-sized (like Big Weevil) and they have a blog post dedicated to them and their entry.</p>
                          <p>Garden of the Week is a contest hosted by the Garden Inspector, both in-game and on Weevil News (The Bin Weevils Blog).</p>
                          <p>Every week a Weevil's garden is selected as Garden of the Week, which is then featured in-game within the garden shop & on Weevil News.</p>
                          <p>To enter garden of the week, you will need to plant seeds and place items in your Nest's garden. Once you've planted enough items and made your garden presentable, you'll be able to take a photo with the special Garden Inspector camera and then submit your entry.</p>
                        </div>

